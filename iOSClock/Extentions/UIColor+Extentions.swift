import Foundation
import UIKit

extension UIColor {
    static let lightRed = UIColor(red: 1, green: 0, blue: 0, alpha: 0.87)
    static let darkRed = UIColor(red: 1, green: 0, blue: 0, alpha: 0.2)
    
    static let lightGreen = UIColor(red: 0, green: 1, blue: 0, alpha: 0.87)
    static let darkGreen = UIColor(red: 0, green: 1, blue: 0, alpha: 0.2)
    static let blackGreen = UIColor(red: 0, green: 1, blue: 0, alpha: 0.1)
    
    static let lightGray = UIColor(red: 1, green: 1, blue: 1, alpha: 0.87)
    static let darkGray = UIColor(red: 1, green: 1, blue: 1, alpha: 0.2)
    
    static let lightCyan = UIColor(red: 0, green: 1, blue: 1, alpha: 0.87)
    static let darkCyan = UIColor(red: 0, green: 1, blue: 1, alpha: 0.2)
}
